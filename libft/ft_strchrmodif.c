#include "libft.h"

int	ft_strchrmodif(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == 39 || s[i] == 34)
			return (i);
		i++;
	}
	return (-1);
}
